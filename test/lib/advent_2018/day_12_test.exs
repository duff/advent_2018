    ..... => .
    #..#. => .
    .#.#. => #
    ##..# => #
    .##.. => .
    ...#. => .
    #.### => .
    ..### => .
    ####. => .
    #.#.. => #
    .##.# => #
    .#... => #
    ##.#. => #
    ....# => .
    ..#.# => #
    #...# => #
    ..##. => .
    .#..# => #
    .#### => .
