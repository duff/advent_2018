defmodule Advent2018.Day12Test do
  use ExUnit.Case, async: true

  alias Advent2018.Day12

  test "part_a" do
    input = """
    ...## => #
    ..#.. => #
    .#... => #
    .#.#. => #
    .#.## => #
    .##.. => #
    .#### => #
    #.#.# => #
    #.### => #
    ##.#. => #
    ##.## => #
    ###.. => #
    ###.# => #
    ####. => #
    """

    assert Day12.part_a("#..#.#..##......###...###", 20, input) == 325
  end

  test "part_a real" do
    pots = "###.#..#..##.##.###.#.....#.#.###.#.####....#.##..#.#.#..#....##..#.##...#.###.#.#..#..####.#.##.#"

    input = """
    #.... => .
    #.##. => #
    ..#.. => .
    #.#.# => .
    .#.## => #
    ...## => #
    ##... => #
    ###.. => #
    #..## => .
    .###. => .
    ###.# => #
    ..... => .
    #..#. => .
    .#.#. => #
    ##..# => #
    .##.. => .
    ...#. => .
    #.### => .
    ..### => .
    ####. => .
    #.#.. => #
    .##.# => #
    .#... => #
    ##.#. => #
    ....# => .
    ..#.# => #
    #...# => #
    ..##. => .
    .#..# => #
    .#### => .
    ##### => #
    ##.## => #
    """

    assert Day12.part_a(pots, 20, input) == 2909
  end

  test "part_b" do
    pots = "###.#..#..##.##.###.#.....#.#.###.#.####....#.##..#.#.#..#....##..#.##...#.###.#.#..#..####.#.##.#"

    input = """
    #.... => .
    #.##. => #
    ..#.. => .
    #.#.# => .
    .#.## => #
    ...## => #
    ##... => #
    ###.. => #
    #..## => .
    .###. => .
